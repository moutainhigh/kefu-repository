package com.scservice.pojo;

public class AdminRole {
	private Long id;

	private Long aid;

	private Long rid;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Long getAid() {
		return aid;
	}

	public void setAid(Long uid) {
		this.aid = uid;
	}

	public Long getRid() {
		return rid;
	}

	public void setRid(Long rid) { this.rid = rid;}
}